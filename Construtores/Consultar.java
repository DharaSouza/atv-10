package Construtores;

public class Consultar {
	String Data;
	String NomePasciente;
	String NomeDentista;
	
	public String getData() {
		return Data;
	}
	public void setData(String data) {
		Data = data;
	}
	public String getNomePasciente() {
		return NomePasciente;
	}
	public void setNomePasciente(String nomePasciente) {
		NomePasciente = nomePasciente;
	}
	public String getNomeDentista() {
		return NomeDentista;
	}
	public void setNomeDentista(String nomeDentista) {
		NomeDentista = nomeDentista;
	}

}
